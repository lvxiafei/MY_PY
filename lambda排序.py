# 常见
l = [5, 4, 6, 7, 1]
print(sorted(l))  # [1, 4, 5, 6, 7]

# 二维列表

ll = [[2, 5], [3, 7], [4, 4], [9, 5]]
print(sorted(ll))  # [[2, 5], [3, 7], [4, 4], [9, 5]]  首元素排列

# 第二个元素排列
print(sorted(ll, key=lambda x: x[1]))  # [[4, 4], [2, 5], [9, 5], [3, 7]]

# 第二个元素逆序排列
print(sorted(ll, key=lambda x: -x[1]))  # [[3, 7], [2, 5], [9, 5], [4, 4]]

# 组合排列：先第二个元素逆序排列，再第一个元素逆序排列
print(sorted(ll, key=lambda x: [-x[1], -x[0]]))  #[[3, 7], [9, 5], [2, 5], [4, 4]]