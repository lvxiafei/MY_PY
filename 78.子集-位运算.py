'''
给定一组不含重复元素的整数数组nums,返回该数组的所有可能子集（不重复）
i:nums = [1,2,3]
o:
[
[3],
[1],
[2],
[1,2,3]
[1,3]
[2,3]
[1,2]
[]
]
分析：
遍历逐个录入
    录入进行处理->每录入一个数的处理方式->遍历组合进行处理

伪代码：
    初始所有组合[[]]
    遍历所有元素：
        遍历所有组合：
            所有组合 = 所有组合 + 新增组合[元素+已存在组合元素]

'''
def subsets(nums):
    res = [[]]
    for i in nums:
        # print(i) # 1,2,3
        for num in res:
            # print(num)
            res = res + [[i] + num]
    return res



print(subsets([1,2,3]))






















